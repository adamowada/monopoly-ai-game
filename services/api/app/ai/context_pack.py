CONTEXT_PACK_SCHEMA_VERSION = "ai-context-pack-v1"
VISIBLE_MEMORY_SCOPES = frozenset({"public", "table", "audit"})
ACTIVE_NEGOTIATION_STATUSES = ("opened", "active", "countered", "accepted")
AIContextPack: TypeAlias = dict[str, Any]


def build_ai_context_pack(
    state: GameState,
    *,
    player_id: str | UUID,
    decision_type: str = "action_decision",
    ai_profile: Mapping[str, Any] | None = None,
    negotiations: Sequence[Mapping[str, Any]] = (),
    negotiation_messages: Sequence[Mapping[str, Any]] = (),
    deals: Sequence[Mapping[str, Any]] = (),
    contracts: Sequence[Mapping[str, Any]] = (),
    obligations: Sequence[Mapping[str, Any]] = (),
    memory_snippets: Sequence[Mapping[str, Any]] = (),
    rule_snippets: Sequence[Mapping[str, Any]] = (),
) -> AIContextPack:
    """Build a deterministic, JSON-safe prompt context for one AI player.

    Legal actions are always generated by the backend rules engine. Callers may supply
    already-retrieved memory and rule snippets, but memory visibility is enforced here
    as a final guard before the context reaches Codex.
    """

    actor_id = str(player_id)
    legal_actions = [action.model_dump(mode="json") for action in list_legal_actions(state, actor_id)]
    pack = {
        "context_pack_schema_version": CONTEXT_PACK_SCHEMA_VERSION,
        "decision_type": decision_type,
        "game_id": state.game_id,
        "player_id": actor_id,
        "public_game_state": _public_game_state_summary(state),
        "timing": _timing_context(state, legal_actions),
        "legal_actions": legal_actions,
        "negotiation_context": {
            "active_negotiations": [
                _public_negotiation(row) for row in _sorted_rows(negotiations)
            ],
            "public_messages": [
                _public_negotiation_message(row)
                for row in _sorted_rows(negotiation_messages)
                if _is_public_negotiation_message(row)
            ],
            "public_deals": [_public_deal(row) for row in _sorted_rows(deals)],
        },
        "contracts": {
            "active_contracts": [_public_contract(row) for row in _sorted_rows(contracts)],
            "active_obligations": [_public_obligation(row) for row in _sorted_rows(obligations)],
        },
        "memory": {
            "visibility_policy": {
                "own_private_memory_visible": True,
                "public_table_audit_memory_visible": True,
                "other_player_private_memory_visible": False,
            },
            "snippets": [
                _memory_snippet(row)
                for row in _sorted_memory_rows(memory_snippets)
                if _memory_visible_to_player(row, actor_id)
            ],
        },
        "rules": {
            "snippets": _rule_snippets(rule_snippets, legal_actions=legal_actions, state=state),
        },
        "personality_profile": _profile_summary(ai_profile),
        "required_output_schema": _json_safe(AI_OUTPUT_SCHEMA),
        "instruction_contract": _instruction_contract(),
    }
    return _json_safe(pack)


async def build_ai_context_pack_from_db(
    session: AsyncSession,
    *,
    game_id: str | UUID,
    player_id: str | UUID,
    state: GameState | None = None,
    session_factory: async_sessionmaker[AsyncSession] | None = None,
    decision_type: str = "action_decision",
    negotiation_id: str | UUID | None = None,
    rule_snippets: Sequence[Mapping[str, Any]] = (),
    max_memory_snippets: int = 12,
    max_negotiations: int = 10,
    max_negotiation_messages: int = 40,
    max_deals: int = 20,
) -> dict[str, Any]:
    """Load visible AI context rows and build a Stage 7.4 context pack."""

    game_uuid = _coerce_uuid(game_id)
    player_uuid = _coerce_uuid(player_id)
    negotiation_uuid = None if negotiation_id is None else _coerce_uuid(negotiation_id)
    if state is None:
        if session_factory is None:
            raise ValueError("session_factory is required when state is not supplied")
        state = await EventPersistence(session_factory).replay_current_state_for_update(
            session,
            game_uuid,
        )

    ai_profile = await _load_ai_profile(session, game_id=game_uuid, player_id=player_uuid)
    memory_rows = await _load_visible_memory_rows(
        session,
        game_id=game_uuid,
        player_id=player_uuid,
        limit=max_memory_snippets,
    )
    negotiation_rows = await _load_negotiation_rows(
        session,
        game_id=game_uuid,
        negotiation_id=negotiation_uuid,
        limit=max_negotiations,
    )
    message_rows = await _load_negotiation_message_rows(
        session,
        game_id=game_uuid,
        negotiation_id=negotiation_uuid,
        limit=max_negotiation_messages,
    )
    deal_rows = await _load_deal_rows(
        session,
        game_id=game_uuid,
        negotiation_id=negotiation_uuid,
        limit=max_deals,
    )
    contract_rows = await _load_contract_rows(session, game_id=game_uuid)
    obligation_rows = await _load_obligation_rows(session, game_id=game_uuid)

    return build_ai_context_pack(
        state,
        player_id=player_uuid,
        decision_type=decision_type,
        ai_profile=ai_profile,
        negotiations=negotiation_rows,
        negotiation_messages=message_rows,
        deals=deal_rows,
        contracts=contract_rows,
        obligations=obligation_rows,
        memory_snippets=memory_rows,
        rule_snippets=rule_snippets,
    )


async def _load_ai_profile(
    session: AsyncSession,
    *,
    game_id: UUID,
    player_id: UUID,
) -> Mapping[str, Any] | None:
    result = await session.execute(
        sa.select(ai_profiles).where(
            ai_profiles.c.game_id == game_id,
            ai_profiles.c.player_id == player_id,
        )
    )
    row = result.mappings().first()
    return None if row is None else dict(row)


async def _load_visible_memory_rows(
    session: AsyncSession,
    *,
    game_id: UUID,
    player_id: UUID,
    limit: int,
) -> list[dict[str, Any]]:
    result = await session.execute(
        sa.select(ai_memory_entries)
        .where(
            ai_memory_entries.c.game_id == game_id,
            sa.or_(
                ai_memory_entries.c.player_id == player_id,
                ai_memory_entries.c.visibility.in_(tuple(sorted(VISIBLE_MEMORY_SCOPES))),
            ),
        )
        .order_by(
            ai_memory_entries.c.importance.desc(),
            ai_memory_entries.c.created_at.desc(),
            ai_memory_entries.c.id.desc(),
        )
        .limit(limit)
    )
    return [dict(row) for row in result.mappings().all()]


async def _load_negotiation_rows(
    session: AsyncSession,
    *,
    game_id: UUID,
    negotiation_id: UUID | None,
    limit: int,
) -> list[dict[str, Any]]:
    statement = sa.select(negotiations).where(
        negotiations.c.game_id == game_id,
        negotiations.c.status.in_(ACTIVE_NEGOTIATION_STATUSES),
    )
    if negotiation_id is not None:
        statement = statement.where(negotiations.c.id == negotiation_id)
    result = await session.execute(
        statement.order_by(
            negotiations.c.updated_at.desc(),
            negotiations.c.created_at.desc(),
            negotiations.c.id.desc(),
        ).limit(limit)
    )
    return [dict(row) for row in result.mappings().all()]


async def _load_negotiation_message_rows(
    session: AsyncSession,
    *,
    game_id: UUID,
    negotiation_id: UUID | None,
    limit: int,
) -> list[dict[str, Any]]:
    statement = sa.select(negotiation_messages).where(
        negotiation_messages.c.game_id == game_id,
        negotiation_messages.c.recipient_player_id.is_(None),
    )
    if negotiation_id is not None:
        statement = statement.where(negotiation_messages.c.negotiation_id == negotiation_id)
    result = await session.execute(
        statement.order_by(
            negotiation_messages.c.created_at.desc(),
            negotiation_messages.c.id.desc(),
        ).limit(limit)
    )
    return [dict(row) for row in result.mappings().all()]


async def _load_deal_rows(
    session: AsyncSession,
    *,
    game_id: UUID,
    negotiation_id: UUID | None,
    limit: int,
) -> list[dict[str, Any]]:
    statement = sa.select(deals).where(deals.c.game_id == game_id)
    if negotiation_id is not None:
        statement = statement.where(deals.c.negotiation_id == negotiation_id)
    result = await session.execute(
        statement.order_by(deals.c.created_at.desc(), deals.c.id.desc()).limit(limit)
    )
    return [dict(row) for row in result.mappings().all()]


async def _load_contract_rows(
    session: AsyncSession,
    *,
    game_id: UUID,
) -> list[dict[str, Any]]:
    result = await session.execute(
        sa.select(contracts)
        .where(contracts.c.game_id == game_id, contracts.c.status == "active")
        .order_by(contracts.c.created_at, contracts.c.id)
    )
    return [dict(row) for row in result.mappings().all()]


async def _load_obligation_rows(
    session: AsyncSession,
    *,
    game_id: UUID,
) -> list[dict[str, Any]]:
    result = await session.execute(
        sa.select(obligations)
        .where(obligations.c.game_id == game_id, obligations.c.status == "pending")
        .order_by(obligations.c.created_at, obligations.c.id)
    )
    return [dict(row) for row in result.mappings().all()]


def _public_game_state_summary(state: GameState) -> dict[str, Any]:
    data = load_classic_monopoly_data()
    spaces_by_position = {space.position: space for space in data.board}
    properties_by_id = {property_data.id: property_data for property_data in data.properties}
    owned_property_ids_by_player = _owned_property_ids_by_player(state)

    return {
        "game_id": state.game_id,
        "ruleset_version": state.ruleset_version,
        "state_hash": state.state_hash(),
        "event_sequence": state.event_sequence,
        "turn": {
            "turn_number": state.turn.turn_number,
            "current_player_index": state.turn.current_player_index,
            "current_player_id": state.turn.current_player_id,
            "phase": state.turn.phase.value,
            "consecutive_doubles": state.turn.consecutive_doubles,
        },
        "players": [
            {
                "id": player.id,
                "name": player.name,
                "kind": player.kind,
                "cash": player.cash,
                "position": player.position,
                "space": _space_summary(spaces_by_position[player.position]),
                "in_jail": player.in_jail,
                "jail_turns": player.jail_turns,
                "get_out_of_jail_card_count": len(player.get_out_of_jail_card_ids),
                "is_bankrupt": player.is_bankrupt,
                "owned_property_ids": owned_property_ids_by_player.get(player.id, []),
            }
            for player in state.players
        ],
        "property_ownership": [
            {
                "property_id": ownership.property_id,
                "name": properties_by_id[ownership.property_id].name,
                "group": properties_by_id[ownership.property_id].group,
                "kind": properties_by_id[ownership.property_id].kind,
                "board_position": properties_by_id[ownership.property_id].board_position,
                "price": properties_by_id[ownership.property_id].price,
                "owner_id": ownership.owner_id,
                "mortgaged": ownership.mortgaged,
                "houses": ownership.houses,
                "hotel": ownership.hotel,
            }
            for ownership in state.property_ownership
        ],
        "bank_inventory": {
            "houses": state.bank_inventory.houses,
            "hotels": state.bank_inventory.hotels,
        },
        "active_payment": None
        if state.active_payment is None
        else state.active_payment.model_dump(mode="json"),
        "active_auction": None
        if state.active_auction is None
        else state.active_auction.model_dump(mode="json"),
        "active_bankruptcy": None
        if state.active_bankruptcy is None
        else state.active_bankruptcy.model_dump(mode="json"),
        "active_atomic_resolution": None
        if state.active_atomic_resolution is None
        else state.active_atomic_resolution.model_dump(mode="json"),
    }


def _timing_context(
    state: GameState,
    legal_actions: Sequence[Mapping[str, Any]],
) -> dict[str, Any]:
    legal_action_types = sorted(
        {str(action["type"]) for action in legal_actions if isinstance(action.get("type"), str)}
    )
    return {
        "active_phase": state.turn.phase.value,
        "current_player_id": state.turn.current_player_id,
        "turn_number": state.turn.turn_number,
        "timing_window": {
            "phase": state.turn.phase.value,
            "backend_legal_action_types": legal_action_types,
        },
        "backend_defined_action_windows": {
            action_type: _phase_values(ACTION_TIMING_WINDOWS[action_type])
            for action_type in legal_action_types
            if action_type in ACTION_TIMING_WINDOWS
        },
    }


def _owned_property_ids_by_player(state: GameState) -> dict[str, list[str]]:
    owned: dict[str, list[str]] = {player.id: [] for player in state.players}
    for ownership in state.property_ownership:
        if ownership.owner_id is not None:
            owned.setdefault(ownership.owner_id, []).append(ownership.property_id)
    return {player_id: sorted(property_ids) for player_id, property_ids in owned.items()}


def _space_summary(space: Any) -> dict[str, Any]:
    return {
        "id": space.id,
        "name": space.name,
        "position": space.position,
        "type": space.type,
        "property_id": space.property_id,
    }


def _public_negotiation(row: Mapping[str, Any]) -> dict[str, Any]:
    context = _mapping(row.get("context"))
    return {
        "id": _string_or_none(row.get("id")),
        "opened_by_player_id": _string_or_none(row.get("opened_by_player_id")),
        "status": _string_or_none(row.get("status")),
        "phase": _string_or_none(row.get("phase")),
        "round_number": row.get("round_number"),
        "participant_player_ids": _string_list(context.get("participant_player_ids")),
        "pending_deal_id": _string_or_none(context.get("pending_deal_id")),
        "current_deal_id": _string_or_none(context.get("current_deal_id")),
        "current_terms_hash": _string_or_none(context.get("current_terms_hash")),
        "context": _mapping(context.get("context")),
        "cutoff_policy": _mapping(context.get("cutoff_policy")),
        "cutoff_reason": _string_or_none(context.get("cutoff_reason")),
        "expired_by_cutoff": bool(context.get("expired_by_cutoff", False)),
        "created_at": _string_or_none(row.get("created_at")),
        "updated_at": _string_or_none(row.get("updated_at")),
        "closed_at": _string_or_none(row.get("closed_at")),
    }


