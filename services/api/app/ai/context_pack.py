def _memory_visible_to_player(row: Mapping[str, Any], actor_id: str) -> bool:
    visibility = str(row.get("visibility", "private"))
    row_player_id = _string_or_none(row.get("player_id"))
    return row_player_id == actor_id or visibility in VISIBLE_MEMORY_SCOPES


def _memory_snippet(row: Mapping[str, Any]) -> dict[str, Any]:
    return {
        "id": _string_or_none(row.get("id")),
        "player_id": _string_or_none(row.get("player_id")),
        "ai_profile_id": _string_or_none(row.get("ai_profile_id")),
        "visibility": _string_or_none(row.get("visibility")) or "private",
        "category": _string_or_none(row.get("category")),
        "content": _string_or_none(row.get("content")),
        "importance": row.get("importance"),
        "metadata": _mapping(row.get("metadata_blob", row.get("metadata"))),
        "source_decision_id": _string_or_none(row.get("source_decision_id")),
        "source_event_id": _string_or_none(row.get("source_event_id")),
        "source_negotiation_message_id": _string_or_none(
            row.get("source_negotiation_message_id")
        ),
        "created_at": _string_or_none(row.get("created_at")),
    }


def _rule_snippets(
    rule_snippets: Sequence[Mapping[str, Any]],
    *,
    legal_actions: Sequence[Mapping[str, Any]],
    state: GameState,
) -> list[dict[str, Any]]:
    if rule_snippets:
        return [_json_safe(dict(snippet)) for snippet in rule_snippets]

    legal_action_types = sorted(
        {str(action["type"]) for action in legal_actions if isinstance(action.get("type"), str)}
    )
    return [
        {
            "id": "backend-rules-authority",
            "source": "backend",
            "text": "The FastAPI backend rules engine is authoritative for legal actions.",
        },
        {
            "id": f"phase-{state.turn.phase.value.lower()}-legal-actions",
            "source": "backend:list_legal_actions",
            "text": (
                f"During {state.turn.phase.value}, this player may choose only from "
                f"these backend-generated legal action types: {', '.join(legal_action_types)}."
            ),
        },
    ]


def _profile_summary(ai_profile: Mapping[str, Any] | None) -> dict[str, Any]:
    if ai_profile is None:
        return {
            "profile_found": False,
            "id": None,
            "player_id": None,
            "persona_name": None,
            "persona_summary": None,
            "strategy_profile": {},
        }

    persona_summary = ai_profile.get("persona_summary")
    summary_text = None
    if isinstance(persona_summary, Mapping):
        summary_text = _string_or_none(persona_summary.get("summary"))
    return {
        "profile_found": True,
        "id": _string_or_none(ai_profile.get("id")),
        "player_id": _string_or_none(ai_profile.get("player_id")),
        "persona_name": _string_or_none(ai_profile.get("persona_name")),
        "persona_summary": summary_text or _string_or_none(persona_summary),
        "strategy_profile": _mapping(ai_profile.get("strategy_profile")),
    }


def _instruction_contract() -> dict[str, Any]:
    return {
        "backend_is_rules_authority": True,
        "choose_only_from_legal_actions": True,
        "do_not_infer_hidden_context": True,
        "do_not_mutate_state_directly": True,
        "return_exactly_one_schema_valid_json_object": True,
        "no_fallback_actions": True,
        "no_default_random_coerced_or_substitute_move": True,
        "invalid_output_will_be_rejected_without_game_mutation": True,
        "required_output_schema_name": "AIDecisionOutput",
        "instructions": [
            "Choose action_decision.action only from legal_actions when making a game action.",
            "Use expected_state_hash and expected_event_sequence from a chosen legal action.",
            "Negotiation text may use only public negotiation context and visible memory snippets.",
            "Do not rely on hidden deck order, RNG state, or another player's private memory.",
            "Return self_dialogue and memory_updates according to the required output schema.",
        ],
    }


def _is_public_negotiation_message(row: Mapping[str, Any]) -> bool:
    return row.get("recipient_player_id") is None


def _sorted_rows(rows: Sequence[Mapping[str, Any]]) -> list[Mapping[str, Any]]:
    return sorted(rows, key=lambda row: (_string_or_none(row.get("created_at")) or "", _row_id(row)))


def _sorted_memory_rows(rows: Sequence[Mapping[str, Any]]) -> list[Mapping[str, Any]]:
    return sorted(
        rows,
        key=lambda row: (
            -_int_or_zero(row.get("importance")),
            _string_or_none(row.get("created_at")) or "",
            _row_id(row),
        ),
    )


def _phase_values(phases: Iterable[TurnPhase]) -> list[str]:
    phase_order = {phase: index for index, phase in enumerate(TurnPhase)}
    return [
        phase.value
        for phase in sorted(phases, key=lambda phase: phase_order.get(phase, len(phase_order)))
    ]


def _string_list(value: Any) -> list[str]:
    if not isinstance(value, Sequence) or isinstance(value, str | bytes | bytearray):
        return []
    return [str(item) for item in value]


def _mapping(value: Any) -> dict[str, Any]:
    return _json_safe(value) if isinstance(value, Mapping) else {}


def _string_or_none(value: Any) -> str | None:
    if value is None:
        return None
    text = str(value)
    return text if text else None


def _int_or_zero(value: Any) -> int:
    if isinstance(value, bool):
        return 0
    if isinstance(value, int):
        return value
    return 0


def _row_id(row: Mapping[str, Any]) -> str:
    return _string_or_none(row.get("id")) or ""


def _coerce_uuid(value: str | UUID) -> UUID:
    if isinstance(value, UUID):
        return value
    return UUID(str(value))


def _json_safe(value: Any) -> Any:
    return json.loads(json.dumps(value, sort_keys=True, default=str, ensure_ascii=True))


