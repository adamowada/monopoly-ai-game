async def _fetch_ai_decision_rows(session_factory: async_sessionmaker) -> list[dict[str, Any]]:
    async with session_factory() as session:
        result = await session.execute(
            sa.select(ai_decisions)
            .where(
                ai_decisions.c.game_id == GAME_ID,
                ai_decisions.c.decision_type == "action_decision",
            )
            .order_by(ai_decisions.c.created_at.desc(), ai_decisions.c.id.desc())
        )
        return [dict(row) for row in result.mappings().all()]


async def _count_events(session_factory: async_sessionmaker) -> int:
    async with session_factory() as session:
        result = await session.execute(
            sa.select(sa.func.count())
            .select_from(game_events)
            .where(game_events.c.game_id == GAME_ID)
        )
        return int(result.scalar_one())


async def _delete_game(session_factory: async_sessionmaker) -> None:
    async with session_factory() as session:
        async with session.begin():
            await session.execute(games.delete().where(games.c.id == GAME_ID))
